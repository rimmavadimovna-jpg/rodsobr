# Парсинг перечня слов из .docx (§5.1)
# --------------------------------------------------------------------------- #
SOURCES_DIR = Path(__file__).resolve().parent / "sources"
WORDLIST_DOCX = SOURCES_DIR / "Перечень_слов_безударные_гласные.docx"


def parse_word_list_docx(path: str | Path = WORDLIST_DOCX) -> list[tuple]:
    """Парсит перечень слов из .docx в строки для таблицы words.

    Ожидаемый формат строки в документе (гибко, через табы/«—»/«-»):
        слово_с_пропуском  <таб|—>  буква  <таб|—>  проверочное/тип

    Возвращает список кортежей под seed_words. Если файла нет или python-docx не
    установлен — возвращает пустой список (тогда используется встроенный seed).
    """
    path = Path(path)
    if not path.exists():
        return []
    try:  # pragma: no cover - опциональная зависимость
        import docx  # python-docx
    except Exception:  # pragma: no cover
        print("python-docx не установлен — перечень .docx пропущен")
        return []
    import re as _re
    doc = docx.Document(str(path))  # pragma: no cover - нужен файл
    rows: list[tuple] = []
    for para in doc.paragraphs:  # pragma: no cover
        line = para.text.strip()
        if not line:
            continue
        parts = [p.strip() for p in _re.split(r"\t|—|–| - ", line) if p.strip()]
        if len(parts) < 2:
            continue
        word, letter = parts[0], parts[1]
        check = parts[2] if len(parts) > 2 else ""
        rows.append((word, "из перечня", letter, check, "перечень", "корень", LOP, 0))
    return rows


