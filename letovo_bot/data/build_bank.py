def seed_words(conn: sqlite3.Connection) -> None:
    """Несколько слов в таблицу words (материал зад. 1, безударные/чередующиеся)."""
    rows = [
        ("г_ра", "проверяемая безударная", "о", "горы", "безударная_проверяемая", "корень", LOP, 1),
        ("тр_ва", "проверяемая безударная", "а", "травы", "безударная_проверяемая", "корень", LOP, 1),
        ("р_сти", "чередование о-а", "а", "корень -раст-/-ращ-", "чередование", "корень", LOP, 1),
        ("заг_рать", "чередование о-а", "о", "корень -гор-/-гар-", "чередование", "корень", LOP, 1),
    ]
    # Плюс перечень из .docx, если он положен в data/sources/ (verified=0 →
    # требует выверки через data.verify.verify_orthograms).
    docx_rows = parse_word_list_docx()
    if docx_rows:
        print(f"  импортировано из .docx: {len(docx_rows)} слов (verified=0)")
        rows = rows + docx_rows
    conn.executemany(
        "INSERT INTO words (word_gapped,hint,answer_letter,check_word,orthogram_type,topic,source,verified)"
        " VALUES (?,?,?,?,?,?,?,?)", rows)
    conn.commit()


